/**
 * Erase Direction enums for the bamboo control:
 * Xminus - sets the erase direction to the left of the object
 * Xplus - sets the erase direction to the right of the object
 * Yminus - sets the erase direction down from the object
 * Yplus - sets the erase direction up from the object
 */
public enum EraseDirection
{
    Xminus = 0,
    Xplus = 1,
    Yminus = 2,
    Yplus = 3
}